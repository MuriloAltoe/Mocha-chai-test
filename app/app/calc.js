
class Calculadora {
    soma(n1, n2) {
        var n3;
        return n3 = n1 + n2;
    };
    subtração(n1, n2) {
        var n3;
        return n3 = n1 - n2;
    };
    multiplicacao(n1, n2) {
        var n3;
        return n3 = n1 * n2;
    };
    divisao(n1, n2) {
        var n3;
        return n3 = n1 / n2;
    };
};

module.exports = Calculadora;

